package main

import (
	"bufio"
	"fmt"
	"os"
	"strconv"
)

func filter(predicate func(int) bool, iterable []int) []int {
	filtered := make([]int, 0)

	for _, v := range iterable {
		if predicate(v) {
			filtered = append(filtered, v)
		}
	}
	return filtered
}

func main() {
	src := readInput()
	res := filter(func(i int) bool { return i%2 == 0 }, src)
	fmt.Println(res)
}

